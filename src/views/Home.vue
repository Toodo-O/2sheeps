<script lang="ts">
import siteConfig from '../config/site'
import { ref } from 'vue'

export default {
  name: 'Home',
  setup() {
    const isSubmitting = ref(false)

    return {
      featuredProjects: siteConfig.featuredProjects,
      latestArticles: siteConfig.latestArticles,
      socialLinks: siteConfig.socialLinks,
      siteConfig,
      form: {
        companyName: '',
        lastName: '',
        firstName: '',
        email: '',
        phone: '',
        message: '',
        contactMethod: 'どちらでも良い',
      },
      isSubmitting,
    }
  },
  methods: {
    async handleSubmit() {
      // 提交状态（可选：用于模板中禁用按钮/显示文案）
      this.isSubmitting = true
      this.submitMessage = ''
      this.submitSuccess = null

      // 组装与示例一致的 payload
      const formData = {
        companyName: this.form.companyName,
        lastName: this.form.lastName,
        firstName: this.form.firstName,
        email: this.form.email,
        phone: this.form.phone,
        projectContent: this.form.message,
        contactMethod: this.form.contactMethod,
      }

      // API 地址：优先使用 Vite 环境变量，回退到占位路径
      const apiUrl = 'https://2sheep-service.vercel.app/api/contact-form'

      try {
        const response = await fetch(apiUrl, {
          method: 'POST',
          headers: {
            'Content-Type': 'application/json',
          },
          body: JSON.stringify(formData),
        })

        let result
        try {
          result = await response.json()
        } catch (e) {
          result = {
            success: response.ok,
            message: response.ok ? '送信しました' : `HTTP ${response.status}`,
          }
        }

        if (result && (result.success === true || response.ok)) {
          this.submitSuccess = true
          this.submitMessage = `✅ ${result.message || '送信成功'}`
          // 重置表单
          this.form = {
            companyName: '',
            lastName: '',
            firstName: '',
            email: '',
            phone: '',
            message: '',
            contactMethod: 'どちらでも良い',
          }
        } else {
          throw new Error((result && result.message) || '提交失败')
        }
      } catch (error) {
        this.submitSuccess = false
        this.submitMessage = `❌ 错误: ${error.message}`
      } finally {
        this.isSubmitting = false
      }
    },
  },
}
</script>

